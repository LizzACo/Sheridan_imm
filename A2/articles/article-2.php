<h1 >Airbnb adds safety support in 11 new languages</h1>
<h2>The vacation rental company's Urgent Support Line was previously available only to people who listed English as their primary language.</h2>
<h3>Andrew Blok</h3>
<h3>Oct. 28, 2021 9:55 a.m. PT</h3>

<img src = "../img/01.png">

<div id = "box">

<p>Vacation rental company Airbnb is expanding a safety feature to some customers whose primary language isn't English, the firm said in a blog post this week.

<p>The company's Urgent Support Line, through which Airbnb's crisis support team helps guests and hosts navigate unsafe situations, had previously been available only to people who listed their primary language as English. It's now available to folks who list Czech, Brazilian Portuguese, Dutch, French, German, Hungarian, Italian, Japanese, Korean, Malay or Spanish.

<p>The step is the latest of several that Airbnb has taken since drawing some scrutiny over the last couple of years concerning safety on the platform.

<p>Airbnb debuted the Urgent Support Line earlier this year. It's available within the app's safety center from 24 hours before check-in to 24 hours after checkout. It promises access within 30 seconds to a member of the crisis support team who's trained to help de-escalate dangerous situations, according to the blog post. In certain locations, the safety center can connect users to local emergency services, a feature that can be useful for travelers in foreign countries.

<p>After five people were killed in a shooting at an Airbnb rental on Halloween in 2019, the company promised to verify 100% of the properties listed on its platform. Verification includes checking that photos, addresses and listing details are accurate and that properties are up to standards of safety, cleanliness and amenities provided.

<p>Airbnb has also imposed restrictions on some reservations of one or two nights around certain holidays, to crack down on potentially dangerous partying. The company says unauthorized parties were down 49% on Halloween and 51% on New Year's Eve.

