<h1>Facebook renames itself Meta amid controversy</h1>
<h2>The social network wants to be known as a metaverse company.</h2>
<h3>Queenie Wong</h3>
<h3>Oct. 28, 2021 12:58 p.m. PT</h3>

<img src = "../img/00.png">

<div id = "box">
<p>Facebook said Thursday it's rebranding itself as Meta to reflect the company's focus on building the metaverse, a virtual world where people work, play, learn and connect with their friends and family.

<p>Meta will be a new corporate brand that will preside over Facebook and its services including photo app Instagram, messaging app WhatsApp and its virtual and augmented reality efforts. Facebook says it will start trading under the new stock ticker MVRS on Dec. 1. 

<p>"In our DNA, we are a company that builds technology to connect people and the metaverse is the next frontier, just like social networking was when we got started," Facebook founder and Meta CEO Mark Zuckerberg said during Facebook Connect, the company's annual VR and AR conference. Zuckerberg said that the word "meta" comes from the Greek word meaning "beyond" and that "it symbolizes that there is always more to build."

<p>In a preview of the metaverse, avatars are playing chess, standing in a virtual concert, learning and working remotely. In the next five to 10 years, Zuckerberg believes, these virtual experiences will become mainstream. In the next decade, he hopes there will be a billion users in the metaverse. 

<p>The widely rumored rebranding underscores how Facebook, founded in 2004, has grown beyond social networking. Facebook owns virtual reality headset maker Oculus, built Portal video chat devices and teamed up with Ray-Ban to release its first pair of smart glasses this year. It's also the most high-profile rebranding of a tech company since 2015, when Google formed its parent company Alphabet.

<p>A new corporate name, though, doesn't fix Facebook's seemingly endless list of problems. For years, the company has dealt with criticism that it doesn't do enough to safeguard privacy, combat hate speech and stop the spread of misinformation. Now the social network is grappling with more allegations that it puts profits over user safety after former Facebook product manager turned whistleblower Frances Haugen leaked internal research to Congress and the US Securities and Exchange Commission. The Wall Street Journal and then a consortium of US and international news outlets published stories based on some of those documents.

