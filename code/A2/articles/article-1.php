Facebook has said its internal research is being mischaracterized to paint a "false picture" of the company. The social network says more than 40,000 people work on safety and security, and the company is on track to spend more than $5 billion on these issues this year.

<p>"Some of you might be wondering why we're doing this right now. The answer is that I believe that we're put on this Earth to create. I believe that technology can make our lives better," Zuckerberg said. 

<p>Facebook's new wave of controversy also hasn't stopped the company from doubling down on the metaverse, which the company sees as the successor to the mobile internet.

<p>The company said Monday it would break out Facebook Reality Labs, which houses its AR and VR products, into its own reporting segment in the fourth quarter. The other segment would include Facebook's apps such as Instagram and WhatsApp. Facebook expects to spend $10 billion on its metaverse efforts this year.

<p>The tech giant introduced more VR and AR tools during Facebook Connect, including a virtual social space for your home, messenger calling in VR, a marketplace for digital goods and a new app called Polar to easily create AR and filters without needing to know how to code. Facebook also said it's working on a higher-end headset known as Cambria and on augmented reality glasses. 

<p>Solving its content moderation and data privacy problems will likely become more challenging as the social network doubles down on immersive environments that go beyond text, photos and video. Facebook said it's looking at ways to prevent people from using another person's avatars, by tying it to an authenticated account or by verifying identity in another way.

<p>"With all the novel technologies that are being developed, everyone who's building for the metaverse should be focused on building responsibly from the beginning," Zuckerberg said. 

