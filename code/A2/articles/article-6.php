<h1>Everyone wants to hire for these 10 jobs — but not enough people are applying</h1>
<h2>These 10 job positions will be hiring like crazy</h2>
<h3>Annie Nova</h3>
<h3>PUBLISHED THU, FEB 15 20188:30 AM ESTUPDATED TUE, FEB 20 20183:23 PM EST</h3>

<img src = "../img/04.png">

<div id = "box">
<p>There are a myriad of ways to decide which jobs to apply for. Not a terrible one is to ask: Who’s hiring?

<p>Employers will be particularly eager to fill certain positions this year, according to CareerCast.com’s 10 toughest jobs to fill for 2018. There are more job openings for some positions — including personal care aides, truck drivers and application software developers — than there are qualified people to fill them.

<p>“There’s a lot of immediate need in all of these positions,” said Kyle Kensing, online content editor at CareerCast.

<p>For example, too few people hold the certificates necessary to be truck drivers, Kensing said. And even as more people live on their phones, not enough people understand how to create digital apps.

<p>Aging baby boomers are also generating a need for health workers (which account for half of the positions on the list below), as well as financial advisors to help them transition into retirement, he said.

<p>If you have the skills and experience to apply for the jobs below, you might get a call back pretty quickly.

<p>1) Application software developer

<p>Companies will need to fill 255,400 application software developer positions by 2026 — representing growth of 31 percent.

<p>The annual median salary is $100,080.

<p>2) Construction worker

<p>In the next eight years, construction jobs will grow by 12 percent, with 150,400 new open positions.

<p>The annual median salary is $33,430.

<p>3) Financial advisor

<p>By 2026, the labor market will need 40,400 more financial advisors. That’s a 15 percent growth from today.

<p>The annual median salary is $90,530.

<p>4) Home health aide 

<p>Home health aides are second only to personal-care aids (see below) in rising demand, with 421,200 spots opening by 2026. That’s a 47 percent increase from current levels.

<p>The annual median salary is $22,600.

<p>5) Information security analyst

<p>There will be some 28,500 openings for information security analysts over the next eight years — a 28 percent increase from now.

<p>The annual median salary is $92,600.

<p>6) Medical service manager

<p>Medical service manager positions will swell by 20 percent in the next eight years, with 72,100 new positions opening up.

<p>The annual median salary is $96,540.

<p>7) Nurse practitioner 

<p>There will be 64,200 nurse practitioners positions that need to be filled by 2026 — which works out to growth of 31 percent.

<p>The annual median salary is $107,460.

<p>8) Personal care aide 

<p>The most new positions will be created for personal care aides, with 777,600 jobs opening up by 2026 — a 39 percent uptick from today.

<p>The annual median salary is $21,920.

<p>9) Physical therapist 

<p>There will be 67,100 physical therapist positions that need to be filled by 2026 — a 28 percent increase from now.

<p>The annual median salary is $85,400.

<p>10) Truck driver 

<p>Truck drivers wanted: 108, 400 of them in the next eight years, a 6 percent growth from today.

<p>The annual median salary is $41,340.

<p>Correction: An earlier version misstated the descriptions of the salaries. They are annual median salaries.<br><br>
